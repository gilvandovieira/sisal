function resolveInitTarget(name: string): InitTarget | undefined {
  const key = name.trim().toLowerCase();
  return INIT_TARGETS.find((target) =>
    target.id === key || target.aliases.includes(key)
  );
}

function initTargetIds(): string {
  return INIT_TARGETS.map((target) => target.id).join(", ");
}

/** Additive DDL generated by a dialect adapter. */
export interface SisalCliGeneratedStatements {
  readonly statements: readonly string[];
  readonly destructive: readonly SchemaChange[];
}

/** Minimal migrator facade used by the CLI. */
export interface SisalCliMigrator {
  migrate(
    options: MigrationRunOptions & {
      readonly migrations: readonly Migration[];
    },
  ): Promise<MigrationResult>;

  plan(options: {
    readonly migrations: readonly Migration[];
  }): Promise<MigrationPlan>;

  applied?(): Promise<unknown>;

  close?(): Promise<void>;
}

/** Dialect adapter hooks used by `sisal` commands. */
export interface SisalCliAdapter {
  generateUpStatements(
    to: SisalSchemaSnapshot,
    from?: SisalSchemaSnapshot,
  ): SisalCliGeneratedStatements;

  createMigrator?(options: {
    readonly config: MigrateConfig;
    readonly dialect: CliDialect;
    readonly logging?: SisalLoggingOptions;
  }): Promise<SisalCliMigrator>;
}

